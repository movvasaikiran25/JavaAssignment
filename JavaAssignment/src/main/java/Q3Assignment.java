public class Q3Assignment {

    public static void main(String[] args) {

        int a, b;
        a = 10;
        b = 90;


        if (a == 10 && b == 70) {
            System.out.println("Square of a given number " + a + " "+ b + "  =  " + ((a * a) + (b * b) + (2 * a * b)));
        } else if (a == 10 && b == 90) {
            System.out.println("Cube of a given number " + a + " "+ b + "  =  " + ((a * a * a) + (b * b * b) + (3 * a * a * b) + (3 * a * b * b)));

        }
    }
}
